public class DikdortgenHesapla {
    public static void main(String[] args) {

        double en = 4.5;
        double boy = 7.9;

        //Alan Hesapla
        double alan = en * boy;

        //Cevre Hesapla
        double cevre = (en * 2) + (boy * 2);

        //Sonucları Yazdır
        System.out.println("Alan: " + alan);
        System.out.println("CEVRE: " + cevre);

    }
}

